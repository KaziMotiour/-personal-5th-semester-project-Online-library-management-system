    private void ScommerceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScommerceActionPerformed
        String query="select * from commerce where code= ?";
        try
        {
            ps=con.prepareStatement(query);
            ps.setString(1, bookcode.getText());
            rs=ps.executeQuery();
            if(rs.next())
            {
                String add=rs.getString("name");
                bname.setText(add);
                String add1 = rs.getString("price");
                bprice.setText(add1);
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_ScommerceActionPerformed

